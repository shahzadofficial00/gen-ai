# Data types in python

# STRING
a= "hello"
print(type(a))

# BOOLEAN
b = 23
print(type(b))

# INTEGER
c= True
print(type(c))

# FLOAT
d = 32.333
print(type(d))

# COMPLEX
e = ["hello","world", "djksn"]
print(type(e), len(e) -1)

# LIST AND ARRAY
f = 1j
print(type(f))

# TYPLES AND CONSTRUCTOR
g = ("hello", "world","!")
print(type(g))

# DICTIONARY
h= {"name": "shahzad", "age": 22}
print(type(h))

# BYTES
i = b"hello world"
print(type(i))

# SET SHUFFLE
j = {"hi", "many" , "fight"}
print(type(j),j)